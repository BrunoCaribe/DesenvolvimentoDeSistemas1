        String doenca;
        double nascimento;

        nomep = (jTextField4.getText());
        p.setNomep(nomep);
        doenca = (jTextField5.getText());
        p.setDoenca(doenca);
        nascimento = (Double.parseDouble(jTextField6.getText()));
